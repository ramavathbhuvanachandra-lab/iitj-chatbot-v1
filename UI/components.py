import streamlit as st


# ---------------------------------------------------------
# Section Divider
# ---------------------------------------------------------

def divider():

    st.divider()


# ---------------------------------------------------------
# Section Title
# ---------------------------------------------------------

def section_title(title: str):

    st.subheader(title)


# ---------------------------------------------------------
# Information Box
# ---------------------------------------------------------

def info_box(message: str):

    st.info(message)


# ---------------------------------------------------------
# Success Box
# ---------------------------------------------------------

def success_box(message: str):

    st.success(message)


# ---------------------------------------------------------
# Warning Box
# ---------------------------------------------------------

def warning_box(message: str):

    st.warning(message)


# ---------------------------------------------------------
# Error Box
# ---------------------------------------------------------

def error_box(message: str):

    st.error(message)